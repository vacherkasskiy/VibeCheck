insert into company_flags (company_id, flag_id, reviews_count, updated_at)
select
    c.id,
    f.id,
    s.reviews_count,
    now()
from seed s
         join companies c on c.name = s.company_name
         join flags f on f.name = s.flag_name
    on conflict (company_id, flag_id) do update
                                             set reviews_count = excluded.reviews_count,
                                             updated_at = excluded.updated_at;